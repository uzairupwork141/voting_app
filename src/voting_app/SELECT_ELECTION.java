     public void getALLDATA()
    {
        int c;
        try {
            
            String id = IDtxt.getSelectedItem().toString();
            
          
            str=con.prepareStatement ("SELECT* from elections where ID='"+id+"'");
            rs=str.executeQuery();
           
            
            if (rs.next()){
               NAMEtxt.setText(rs.getString("NAME"));
               DATEtxt.setText(rs.getString("DATE"));
               totalCandidate.setText(""+getNoOfCandidates()) ;
            }
            else{
                Clear();
            }
            
        } catch (Exception ex) {
           JOptionPane.showMessageDialog(this,ex,"CONNECTION ",3);
        }
    }
    
    
    
    
    public int getNoOfCandidates()
    {
        int c;
        try {
           
          
            str=con.prepareStatement ("SELECT COUNT(*) from candidate_detail where EID='"+IDtxt.getSelectedItem()+"'");
            rs=str.executeQuery();
            if (rs.next()) {                
                c=rs.getInt("count(*)");
                System.out.println();
                return c;
            }
            
        } catch (Exception ex) {
           JOptionPane.showMessageDialog(this,ex,"CONNECTION ",3);
        }
        return 0;
    }
    
    
    
    
