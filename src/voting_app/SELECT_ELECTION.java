    public void getcategory(){
        
        try{
            str=con.prepareStatement("select* from `elections`");
            rs=str.executeQuery();
            while(rs.next()){
                IDtxt.addItem(rs.getString("ID"));
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }
    
    
    
